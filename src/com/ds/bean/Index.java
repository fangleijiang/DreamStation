package com.ds.bean;

public class Index {
private int id;
private String pic_name;
private String pic_description;
private String pic_address;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getPic_name() {
	return pic_name;
}
public void setPic_name(String pic_name) {
	this.pic_name = pic_name;
}
public String getPic_description() {
	return pic_description;
}
public void setPic_description(String pic_description) {
	this.pic_description = pic_description;
}
public String getPic_address() {
	return pic_address;
}
public void setPic_address(String pic_address) {
	this.pic_address = pic_address;
}

}
